#!/usr/bin/env node
import fs from 'fs';
import path from 'path';

// Criar um PNG simples usando buffer bruto (100x100 pixels)
// Logo PNG simples - quadrado vermelho
const pngHeader = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
  0x00, 0x00, 0x00, 0x0d, // IHDR chunk size
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x64, // width: 100
  0x00, 0x00, 0x00, 0x64, // height: 100
  0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, compression
  0x00, 0xd5, 0xce, 0xf3, 0x18, // CRC
]);

const pngData = Buffer.from([
  0x00, 0x00, 0x04, 0x00,
  0x49, 0x44, 0x41, 0x54,
  0x78, 0x9c, 0xed, 0xc1, 0x01, 0x0d, 0x00, 0x00, 0x00, 0xc2, 0xa0, 0xf5, 0x4f, 0xed, 0x61, 0x00,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
  0xf5, 0x0f, 0x3d, 0x32, 0x1d, 0xb8, 0x14, 0x72,
]);

const pngEnd = Buffer.from([
  0x00, 0x00, 0x00, 0x00,
  0x49, 0x45, 0x4e, 0x44,
  0xae, 0x42, 0x60, 0x82,
]);

const png = Buffer.concat([pngHeader, pngData, pngEnd]);

const iconPath = path.join(process.cwd(), 'public', 'icon.png');
fs.mkdirSync(path.dirname(iconPath), { recursive: true });
fs.writeFileSync(iconPath, png);
console.log(`✅ Icon criado: ${iconPath}`);
